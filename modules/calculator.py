class Calculator():
    def __init__(self, name):
        self.name= name



    def add_two_numbers(self, number_1,number_2):
        return number_1 + number_2

    def subtract_two_numbers(self, number_1,number_2):
        return number_1 - number_2
    
    def multiply_two_numbers(self, number_1,number_2):
        return number_1 * number_2

    def divide_two_numbers(self, number_1,number_2):
        return number_1 / number_2